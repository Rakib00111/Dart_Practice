void main()
{
  // declaring a integer variable
  int RollNum =  90014;

// Evaluate the test-expression to find the match
  switch (RollNum) {
    case 90009:
      print("My name is Joseph");
      break;
    case 90010:
      print("My name is Peter");
      break;
    case 090011:
      print("My name is Devansh");
      break;

// default block
    default:
      print("Roll number is not found");
  }
}
